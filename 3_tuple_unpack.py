###############################################################
#####元组拆包
#####*代表剩下的元素，_相当于占位符，去除用。
###############################################################
tuple0=('allen','male','38')
name,gender,age=tuple0
print (name+":"+gender+","+age)
a,b,*rest=range(5)
print (rest)
c,_=(0,1)
print (c)
x,*_,y=(1,2,3,4,5)
print (y)